<template lang="html">
  <div class="markdown" v-html="markdown"></div>
</template>

<script>
import marked from '../../../node_modules/marked'

export default {
  props: ['rawMarkdown'],
  created () {
    marked.setOptions({
      renderer: new marked.Renderer(),
      gfm: true,
      tables: true,
      breaks: false,
      pedantic: false,
      sanitize: false,
      smartLists: true,
      smartypants: false
    })
  },
  data () {
    return {
      markdown: `${marked(this.rawMarkdown)}`
    }
  }
}
</script>

